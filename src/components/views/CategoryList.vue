  const confirmed = await showConfirm({
    title: '快速撤销',
    message: `确定要撤销以下操作吗？\n\n${latestLog.action}`,
    confirmButtonText: '确定撤销'
  })

  if (confirmed) {
    equipmentStore.quickUndo()
  }
}

